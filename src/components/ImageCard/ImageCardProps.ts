/**
 * Props for the ImageCard component.
 */
interface ImageCardProps {
  /**
   * The URL of the image to be displayed in the card.
   */
  image: string;

  /**
   * The title of the image card.
   */
  title: string;

  /**
   * The description or content of the image card.
   */
  description: string;
}

export default ImageCardProps;
